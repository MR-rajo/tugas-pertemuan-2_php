<?php 

// Pengulangan

// For
// While
// Do While
// Foreach

// Langkah-langkah melakukan pengulangan
// 1. menentukan nilai awal
// 2. menentukan kondisi terminasi
// 3. menuliskan increment / decrement

// For
for($nilaiAwal = 1; $nilaiAwal <= 10; $nilaiAwal++){
    echo "Angka ke - $nilaiAwal <br/>";
}

echo "<br/>";
echo "<hr/>";

// While
$noAngkot = 1;
while($noAngkot <= 25){
    echo "Angkot ke - $noAngkot <br/>";

    $noAngkot++;
}

?>